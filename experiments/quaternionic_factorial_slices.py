#!/usr/bin/env python3
"""Numerical baseline for the scalar quaternionic factorial.

This script evaluates

    F(a + r*n) = Gamma(a + 1 + i*r)

and lifts the complex imaginary coefficient onto a chosen unit imaginary
quaternion axis n. It checks axis covariance, cross-slice commutators, and
standard Gamma identities without introducing AGQF factors or well potentials.

Dependencies:
    python -m pip install mpmath numpy matplotlib

Outputs are written to experiments/output/quaternionic-factorial/.
"""

from __future__ import annotations

import csv
import math
from pathlib import Path
from typing import Callable

import matplotlib.pyplot as plt
import mpmath as mp
import numpy as np


OUTPUT_DIR = Path(__file__).resolve().parent / "output" / "quaternionic-factorial"
OUTPUT_DIR.mkdir(parents=True, exist_ok=True)
mp.mp.dps = 50

Quaternion = np.ndarray


def qmul(left: Quaternion, right: Quaternion) -> Quaternion:
    """Hamilton product for arrays [scalar, i, j, k]."""
    left_scalar = float(left[0])
    right_scalar = float(right[0])
    left_vector = np.asarray(left[1:], dtype=float)
    right_vector = np.asarray(right[1:], dtype=float)

    return np.concatenate(
        (
            [left_scalar * right_scalar - np.dot(left_vector, right_vector)],
            left_scalar * right_vector
            + right_scalar * left_vector
            + np.cross(left_vector, right_vector),
        )
    )


def qconj(value: Quaternion) -> Quaternion:
    """Quaternion conjugate."""
    value = np.asarray(value, dtype=float)
    return np.concatenate(([value[0]], -value[1:]))


def qnorm(value: Quaternion) -> float:
    """Euclidean quaternion norm."""
    return float(np.linalg.norm(value))


def qinv(value: Quaternion) -> Quaternion:
    """Quaternion inverse for a nonzero value."""
    value = np.asarray(value, dtype=float)
    denominator = float(np.dot(value, value))
    if denominator == 0.0:
        raise ZeroDivisionError("cannot invert the zero quaternion")
    return qconj(value) / denominator


def qcomm(left: Quaternion, right: Quaternion) -> Quaternion:
    """Quaternion commutator [left, right]."""
    return qmul(left, right) - qmul(right, left)


def unit_axis(axis: Quaternion) -> np.ndarray:
    """Normalize a three-component imaginary-axis vector."""
    axis = np.asarray(axis, dtype=float)
    norm = float(np.linalg.norm(axis))
    if norm == 0.0:
        raise ValueError("imaginary axis must be nonzero")
    return axis / norm


def quaternionic_factorial(a: float, r: float, axis: Quaternion) -> Quaternion:
    """Return the intrinsic slice value F(a+r*n)=Gamma(a+1+i*r)."""
    gamma_value = mp.gamma(mp.mpc(a + 1.0, r))
    direction = unit_axis(axis)
    return np.concatenate(
        (
            [float(mp.re(gamma_value))],
            float(mp.im(gamma_value)) * direction,
        )
    )


def slice_sin_pi(a: float, r: float, axis: Quaternion) -> Quaternion:
    """Return sin(pi*(a+r*n)) in the slice generated by axis."""
    value = mp.sin(mp.pi * mp.mpc(a, r))
    direction = unit_axis(axis)
    return np.concatenate(
        ([float(mp.re(value))], float(mp.im(value)) * direction)
    )


def integral_factorial(a: float, r: float) -> complex:
    """Evaluate the defining integral on one complex slice."""
    integrand: Callable[[mp.mpf], mp.mpc] = (
        lambda x: mp.e ** (-x) * mp.power(x, mp.mpc(a, r))
    )
    return complex(mp.quad(integrand, [0, 1, mp.inf]))


def write_csv(path: Path, fieldnames: list[str], rows: list[dict[str, object]]) -> None:
    """Write dictionaries to CSV with stable column order."""
    with path.open("w", newline="", encoding="utf-8") as handle:
        writer = csv.DictWriter(handle, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)


def main() -> None:
    a = 0.5
    r_values = np.linspace(0.0, 15.0, 601)

    axis_i = np.array([1.0, 0.0, 0.0])
    axis_j = np.array([0.0, 1.0, 0.0])
    axis_k = np.array([0.0, 0.0, 1.0])
    axis_diagonal = np.array([1.0, 1.0, 1.0]) / math.sqrt(3.0)

    # Slice magnitude and phase.
    gamma_values = np.array(
        [complex(mp.gamma(mp.mpc(a + 1.0, float(r)))) for r in r_values]
    )
    magnitudes = np.abs(gamma_values)
    phases = np.unwrap(np.angle(gamma_values))

    profile_rows = [
        {
            "r": float(r),
            "real_part": float(value.real),
            "imaginary_coefficient": float(value.imag),
            "magnitude": float(magnitude),
            "slice_phase": float(phase),
        }
        for r, value, magnitude, phase in zip(
            r_values,
            gamma_values,
            magnitudes,
            phases,
            strict=True,
        )
    ]
    write_csv(
        OUTPUT_DIR / "slice-profile.csv",
        ["r", "real_part", "imaginary_coefficient", "magnitude", "slice_phase"],
        profile_rows,
    )

    plt.figure(figsize=(10, 5.5))
    plt.plot(r_values, magnitudes)
    plt.yscale("log")
    plt.xlabel("imaginary radius r")
    plt.ylabel("|F(1/2 + r I)|")
    plt.title("Quaternionic Factorial Magnitude Along One Slice")
    plt.tight_layout()
    plt.savefig(OUTPUT_DIR / "magnitude.png", dpi=220)
    plt.close()

    plt.figure(figsize=(10, 5.5))
    plt.plot(r_values, phases)
    plt.xlabel("imaginary radius r")
    plt.ylabel("unwrapped slice phase")
    plt.title("Quaternionic Factorial Phase Along One Slice")
    plt.tight_layout()
    plt.savefig(OUTPUT_DIR / "phase.png", dpi=220)
    plt.close()

    # Axis covariance.
    axes = {
        "I": axis_i,
        "J": axis_j,
        "K": axis_k,
        "(I+J+K)/sqrt(3)": axis_diagonal,
    }
    axis_test_r = 3.0
    axis_rows: list[dict[str, object]] = []
    for name, axis in axes.items():
        value = quaternionic_factorial(a, axis_test_r, axis)
        imaginary_vector = value[1:]
        imaginary_norm = float(np.linalg.norm(imaginary_vector))
        output_direction = (
            imaginary_vector / imaginary_norm
            if imaginary_norm > 0.0
            else np.zeros(3, dtype=float)
        )
        axis_rows.append(
            {
                "axis": name,
                "scalar_part": float(value[0]),
                "i_component": float(value[1]),
                "j_component": float(value[2]),
                "k_component": float(value[3]),
                "norm": qnorm(value),
                "output_axis_alignment": float(
                    np.dot(output_direction, unit_axis(axis))
                ),
            }
        )
    write_csv(
        OUTPUT_DIR / "axis-covariance.csv",
        [
            "axis",
            "scalar_part",
            "i_component",
            "j_component",
            "k_component",
            "norm",
            "output_axis_alignment",
        ],
        axis_rows,
    )

    # Rotating imaginary axis.
    theta = np.linspace(0.0, 2.0 * math.pi, 400)
    rotating_axes = np.column_stack(
        [np.cos(theta), np.sin(theta), np.zeros_like(theta)]
    )
    rotating_values = np.array(
        [quaternionic_factorial(a, axis_test_r, axis) for axis in rotating_axes]
    )

    figure = plt.figure(figsize=(8, 6))
    plot = figure.add_subplot(111, projection="3d")
    plot.plot(
        rotating_values[:, 1],
        rotating_values[:, 2],
        rotating_values[:, 3],
    )
    plot.set_xlabel("i component")
    plot.set_ylabel("j component")
    plot.set_zlabel("k component")
    plot.set_title("Imaginary Factorial Output Follows the Input Axis")
    plt.tight_layout()
    plt.savefig(OUTPUT_DIR / "axis-rotation.png", dpi=220)
    plt.close()

    # Cross-slice noncommutativity.
    commutator_rows: list[dict[str, object]] = []
    normalized_values: list[float] = []
    for r in r_values:
        value_i = quaternionic_factorial(a, float(r), axis_i)
        value_j = quaternionic_factorial(a, float(r), axis_j)
        commutator = qcomm(value_i, value_j)
        denominator = qnorm(value_i) * qnorm(value_j)
        normalized = qnorm(commutator) / denominator if denominator else 0.0
        normalized_values.append(normalized)
        commutator_rows.append(
            {
                "r": float(r),
                "commutator_norm": qnorm(commutator),
                "normalized_commutator": normalized,
            }
        )
    write_csv(
        OUTPUT_DIR / "commutator-profile.csv",
        ["r", "commutator_norm", "normalized_commutator"],
        commutator_rows,
    )

    plt.figure(figsize=(10, 5.5))
    plt.plot(r_values, normalized_values)
    plt.xlabel("imaginary radius r")
    plt.ylabel("||[F_I,F_J]|| / (||F_I|| ||F_J||)")
    plt.title("Cross-Slice Quaternionic Factorial Noncommutativity")
    plt.tight_layout()
    plt.savefig(OUTPUT_DIR / "commutator.png", dpi=220)
    plt.close()

    # Integral and identity checks.
    identity_rows: list[dict[str, object]] = []
    for r in [0.0, 0.5, 1.0, 3.0, 6.0]:
        q = np.array([a, r, 0.0, 0.0])
        value = quaternionic_factorial(a, r, axis_i)

        integral_value = integral_factorial(a, r)
        gamma_value = complex(mp.gamma(mp.mpc(a + 1.0, r)))
        integral_error = abs(integral_value - gamma_value)

        next_value = quaternionic_factorial(a + 1.0, r, axis_i)
        recurrence_right = qmul(
            np.array([a + 1.0, r, 0.0, 0.0]),
            value,
        )
        recurrence_error = qnorm(next_value - recurrence_right)

        conjugate_value = quaternionic_factorial(a, -r, axis_i)
        conjugation_error = qnorm(conjugate_value - qconj(value))

        negative_value = quaternionic_factorial(-a, -r, axis_i)
        reflection_left = qmul(value, negative_value)
        reflection_right = qmul(
            math.pi * q,
            qinv(slice_sin_pi(a, r, axis_i)),
        )
        reflection_error = qnorm(reflection_left - reflection_right)

        identity_rows.append(
            {
                "r": r,
                "integral_vs_gamma_error": integral_error,
                "recurrence_error": recurrence_error,
                "conjugation_error": conjugation_error,
                "reflection_error": reflection_error,
            }
        )

    write_csv(
        OUTPUT_DIR / "identity-checks.csv",
        [
            "r",
            "integral_vs_gamma_error",
            "recurrence_error",
            "conjugation_error",
            "reflection_error",
        ],
        identity_rows,
    )

    max_index = int(np.argmax(normalized_values))
    print("Quaternionic factorial baseline complete")
    print(f"output directory: {OUTPUT_DIR}")
    print(
        "maximum sampled normalized I/J commutator: "
        f"{normalized_values[max_index]:.8f} at r={r_values[max_index]:.3f}"
    )


if __name__ == "__main__":
    main()
